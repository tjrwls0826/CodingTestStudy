A = 'abcd'
print(A.upper())
print(A.capitalize())
print(A.title())

c = 'a2b3C4'
print(c.upper())
print(c.capitalize())
print(c.title())

d = 'abc-def gh'
print(d.upper())
print(d.capitalize())
print(d.title())

'''
upper	    주어진 문자열에서 모든 알파벳들을 대문자로 변환시킨다.
capitalize	주어진 문자열에서 맨 첫 글자를 대문자로 변환시킨다.
title	    주어진 문자열에서 알파벳 외의 문자(숫자, 특수기호, 띄어쓰기 등)로 나누어져 있는 영단어들의 첫 글자를 모두 대문자로 변환시킨다.
'''
